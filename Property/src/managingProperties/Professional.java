package managingProperties;

import java.util.LinkedList;
import java.util.List;

public class Professional {

	private String professione;
	private String id;
	private List<Request> richieste = new LinkedList<>();

	public Professional(String profession, String id) {
		this.professione=profession;
		this.id=id;
	}

	public String getProfessione() {
		return professione;
	}

	public String getId() {
		return id;
	}

	public void assign(Request requestN) {
		richieste.add(requestN);
	}

}
